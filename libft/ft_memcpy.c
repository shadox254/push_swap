#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t size)
{
	size_t	i;
	char	*d;
	char	*s;

	i = 0;
	d = (char *)dest;
	s = (char *)src;
	if (!dest && !src)
		return (0);
	if (size == 0)
		return (dest);
	while (i < size)
	{
		d[i] = s[i];
		i++;
	}
	return (dest);
}
