#include "libft.h"

int	ft_memcmp(const void *str1, const void *str2, size_t size)
{
	size_t			i;
	unsigned char	*s1;
	unsigned char	*s2;

	i = 0;
	s1 = (unsigned char *)str1;
	s2 = (unsigned char *)str2;
	if (size == 0)
		return (0);
	while (s1[i] == s2[i] && i < size - 1)
		i++;
	return ((int)(s1[i] - s2[i]));
}
